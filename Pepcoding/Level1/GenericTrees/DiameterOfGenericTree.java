import java.util.*;

class TreeNode {
  int data;
  ArrayList<TreeNode> children;

  TreeNode(int data) {
    this.data = data;
    children = new ArrayList<>();
  }
}

public class Main {

  public static void display(TreeNode root) {
    String str = root.data + " -> ";
    for (TreeNode child : root.children) {
      str += child.data + ", ";
    }
    str += ".";
    System.out.println(str);

    for (TreeNode child : root.children) {
      display(child);
    }
  }

  public static TreeNode construct(int[] arr) {
    TreeNode root = null;
    Stack<TreeNode> st = new Stack<>();

    for (int i = 0; i < arr.length; i++) {
      if (arr[i] == -1) {
        st.pop();
      } else {
        TreeNode node = new TreeNode(arr[i]);

        if (st.size() > 0) {
          st.peek().children.add(node);
        } else {
          root = node;
        }

        st.push(node);
      }
    }

    return root;
  }

  static int diameter = 0;

  public static int findDiameter(TreeNode root) {
    if (root == null) {
      return -1;
    }

    int height = -1;
    int secondaryHeight = -1;

    for (TreeNode child : root.children) {
      int childHeight = findDiameter(child);

      if (childHeight >= height) {
        secondaryHeight = height;
        height = childHeight;
      } else if (childHeight >= secondaryHeight) {
        secondaryHeight = childHeight;
      }
    }

    diameter = Math.max(height + secondaryHeight + 2, diameter);

    return height + 1;
  }

  public static void main(String[] args) {
    Scanner s = new Scanner(System.in);

    int n = s.nextInt();
    int[] arr = new int[n];
    for (int i = 0; i < n; i++) {
      arr[i] = s.nextInt();
    }

    TreeNode root = construct(arr);

    findDiameter(root);
    System.out.println(diameter);

    // display(root);
  }

}
